"""Noisy BAS dataset.

Samples are generated by first drawing a clean Bars-and-Stripes pattern and then
independently flipping each bit with probability ``flip_prob``.
"""

import numpy as np
import matplotlib.pyplot as plt

from .bas import BarsAndStripesDataset


class NoisyBASDataset(BarsAndStripesDataset):
    """Bars-and-Stripes with independent bit-flip noise.

    For ``flip_prob == 0`` this reduces to the clean BAS dataset.
    For ``flip_prob > 0`` the induced distribution has (in principle) full
    support on all bitstrings of length ``height * width``.
    """

    def __init__(self, height: int = 3, width: int = 3, flip_prob: float = 0.05):
        super().__init__(height=height, width=width)
        self.flip_prob = float(flip_prob)
        if not (0.0 <= self.flip_prob <= 1.0):
            raise ValueError(f"flip_prob must be in [0, 1], got {self.flip_prob}")

    def generate(self, n_samples: int, seed: int = 0) -> np.ndarray:
        """Generate noisy BAS samples."""
        clean = super().generate(n_samples=n_samples, seed=seed)
        if self.flip_prob <= 0.0:
            return clean

        rng = np.random.default_rng(seed + 7919)
        flips = rng.random(clean.shape) < self.flip_prob
        noisy = np.bitwise_xor(clean.astype(np.int8), flips.astype(np.int8)).astype(np.int8)
        self.data = noisy
        return self.data

    def validity_rate(self, samples: np.ndarray) -> float:
        """Validity is only meaningful against clean BAS at zero noise."""
        if self.flip_prob > 0.0:
            return 1.0
        return super().validity_rate(samples)

    def coverage_rate(self, ground_truth: np.ndarray, samples: np.ndarray) -> float:
        """Coverage is only meaningful against clean BAS at zero noise."""
        if self.flip_prob > 0.0:
            return 1.0
        return super().coverage_rate(ground_truth, samples)

    def visualize(self, sample: np.ndarray, ax=None):
        """Visualize sample as a 2D binary grid."""
        if ax is None:
            fig, ax = plt.subplots(figsize=(4, 4))

        img = sample.reshape(self.height, self.width)
        ax.imshow(img, cmap='binary', interpolation='nearest')
        ax.set_xticks([])
        ax.set_yticks([])

        for i in range(self.height + 1):
            ax.axhline(i - 0.5, color='gray', linewidth=0.5)
        for j in range(self.width + 1):
            ax.axvline(j - 0.5, color='gray', linewidth=0.5)

        ax.set_title(f"Noisy BAS (p={self.flip_prob:g})")
        return ax