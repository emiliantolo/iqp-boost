"""Noisy BAS dataset.

Samples are generated by first drawing a clean Bars-and-Stripes pattern and then
independently flipping each bit with probability ``flip_prob``.

When ``flip_prob > 0`` the exact analytical probability vector ``self.probs``
is computed over the full :math:`2^N` state space via bit-flip convolution:

.. math::

    p(x) = \\frac{1}{K} \\sum_{s \\in \\text{BAS}}
           (1 - p)^{N - d_H(x, s)} \\cdot p^{d_H(x, s)}

where :math:`d_H` is the Hamming distance and :math:`K` is the number of
valid BAS patterns.
"""

import numpy as np
import matplotlib.pyplot as plt
import jax
import jax.numpy as jnp

from .bas import BarsAndStripesDataset


class NoisyBASDataset(BarsAndStripesDataset):
    """Bars-and-Stripes with independent bit-flip noise.

    For ``flip_prob == 0`` this reduces to the clean BAS dataset.
    For ``flip_prob > 0`` the induced distribution has (in principle) full
    support on all bitstrings of length ``height * width``.

    Attributes:
        probs (np.ndarray): Exact analytical probability vector of shape
            ``(2**n_qubits,)`` over the computational basis.
    """

    def __init__(self, height: int = 3, width: int = 3, flip_prob: float = 0.05,
                 batch_size: int = 2**18):
        super().__init__(height=height, width=width)
        self.flip_prob = float(flip_prob)
        if not (0.0 <= self.flip_prob <= 1.0):
            raise ValueError(f"flip_prob must be in [0, 1], got {self.flip_prob}")
        self.batch_size = batch_size

        # Compute exact analytical probability vector
        self.probs = self._compute_noisy_probs()

    def _compute_noisy_probs(self) -> np.ndarray:
        """Compute the exact noisy BAS probability vector via bit-flip convolution.

        For each bitstring x in {0,1}^N, the probability is:
            p(x) = (1/K) * sum_{s in BAS} (1-flip)^(N - dH(x,s)) * flip^dH(x,s)

        Uses batched JAX computation for efficiency.
        """
        N = self.n_qubits
        total_states = 2 ** N
        p = self.flip_prob

        # Convert valid BAS patterns to integer indices
        bas_patterns = sorted(self._valid_patterns)
        K = len(bas_patterns)
        bas_ints = jnp.array([
            int("".join(str(b) for b in pat), 2) for pat in bas_patterns
        ], dtype=jnp.int32)

        if p <= 0.0:
            # Delta distribution on BAS patterns
            probs = np.zeros(total_states, dtype=np.float64)
            for idx in np.asarray(bas_ints):
                probs[int(idx)] = 1.0 / K
            return probs

        log_p = float(jnp.log(p))
        log_1mp = float(jnp.log(1.0 - p))
        bs = min(self.batch_size, total_states)

        @jax.jit
        def get_batch_probs(start_idx):
            # Enumerate bitstrings in this batch
            x_ints = start_idx + jnp.arange(bs, dtype=jnp.int32)  # (bs,)

            # XOR each bitstring with each BAS pattern -> Hamming distances
            # x_ints: (bs,), bas_ints: (K,) -> xored: (bs, K)
            xored = jnp.bitwise_xor(x_ints[:, None], bas_ints[None, :])

            # Popcount via bit manipulation (count differing bits = Hamming dist)
            dH = jnp.zeros_like(xored, dtype=jnp.float32)
            for bit in range(N):
                dH = dH + jnp.bitwise_and(jnp.right_shift(xored, bit), 1).astype(jnp.float32)

            # Log probability: dH * log(p) + (N - dH) * log(1-p) - log(K)
            log_probs_per_pattern = dH * log_p + (N - dH) * log_1mp  # (bs, K)
            # Average over BAS patterns (uniform 1/K weight)
            log_avg = jax.scipy.special.logsumexp(log_probs_per_pattern, axis=1) - jnp.log(K)
            return log_avg

        all_log_probs = []
        for start in range(0, total_states, bs):
            batch_lp = get_batch_probs(start)
            remaining = total_states - start
            if remaining < bs:
                batch_lp = batch_lp[:remaining]
            all_log_probs.append(batch_lp)

        log_probs = jnp.concatenate(all_log_probs)
        # Normalise (should already be normalised, but ensure numerical stability)
        log_Z = jax.scipy.special.logsumexp(log_probs)
        probs = jnp.exp(log_probs - log_Z)
        return np.asarray(probs, dtype=np.float64)

    def generate(self, n_samples: int, seed: int = 0) -> np.ndarray:
        """Sample bitstrings from the exact noisy BAS distribution.

        Args:
            n_samples: Number of samples to draw.
            seed: Random seed for reproducibility.

        Returns:
            Array of shape ``(n_samples, n_qubits)`` with binary values.
        """
        rng = np.random.default_rng(seed)
        p = np.asarray(self.probs, dtype=np.float64)
        p = p / p.sum()
        indices = rng.choice(len(p), size=n_samples, p=p)

        # Integer-to-bitstring conversion
        bit_positions = np.arange(self.n_qubits)
        samples = ((indices[:, None] >> bit_positions) & 1).astype(np.int8)

        self.data = samples
        return self.data

    def validity_rate(self, samples: np.ndarray) -> float:
        """Validity is only meaningful against clean BAS at zero noise."""
        if self.flip_prob > 0.0:
            return 1.0
        return super().validity_rate(samples)

    def coverage_rate(self, ground_truth: np.ndarray, samples: np.ndarray) -> float:
        """Coverage is only meaningful against clean BAS at zero noise."""
        if self.flip_prob > 0.0:
            return 1.0
        return super().coverage_rate(ground_truth, samples)

    def visualize(self, sample: np.ndarray, ax=None):
        """Visualize sample as a 2D binary grid."""
        if ax is None:
            fig, ax = plt.subplots(figsize=(4, 4))

        img = sample.reshape(self.height, self.width)
        ax.imshow(img, cmap='binary', interpolation='nearest')
        ax.set_xticks([])
        ax.set_yticks([])

        for i in range(self.height + 1):
            ax.axhline(i - 0.5, color='gray', linewidth=0.5)
        for j in range(self.width + 1):
            ax.axvline(j - 0.5, color='gray', linewidth=0.5)

        ax.set_title(f"Noisy BAS (p={self.flip_prob:g})")
        return ax